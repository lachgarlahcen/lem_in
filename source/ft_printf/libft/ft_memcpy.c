#include <string.h>

void	*ft_memcpy(void *str1, const void *str2, size_t n)
{
	char	*s1;
	char	*s2;
	size_t	i;

	s1 = (char *)str1;
	s2 = (char *)str2;
	i = 0;
	while (i < n)
	{
		s1[i] = s2[i];
		i++;
	}
	str1 = (void *)s1;
	return (str1);
}
