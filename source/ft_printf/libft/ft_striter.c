#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	char	*p;
	int		i;

	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			p = &s[i];
			f(p);
			i++;
		}
	}
}
