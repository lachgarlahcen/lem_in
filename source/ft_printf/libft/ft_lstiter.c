#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list *elem;
	t_list *next;

	if (lst)
	{
		elem = lst;
		while (elem)
		{
			next = elem->next;
			f(elem);
			elem = next;
		}
	}
}
