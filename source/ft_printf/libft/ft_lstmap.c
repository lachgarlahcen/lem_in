#include "libft.h"

t_list	*ft_lstmap(t_list *lst, t_list *(*f)(t_list *elem))
{
	t_list	*re;

	if (lst)
	{
		re = f(lst);
		re->next = ft_lstmap(lst->next, f);
		return (re);
	}
	return (NULL);
}
